/**
 * Pre-built crossword grid patterns.
 * '#' = black cell, '.' = white cell.
 * All patterns are 180-degree rotationally symmetric (standard crossword convention).
 */

const PATTERNS_5X5 = [
  // Open pattern with corner blacks
  [
    "#....",
    ".....",
    ".....",
    ".....",
    "....#",
  ],
  // Center cross pattern
  [
    ".#...",
    ".....",
    "..#..",
    ".....",
    "...#.",
  ],
  // Diamond pattern
  [
    ".#.#.",
    ".....",
    "#...#",
    ".....",
    ".#.#.",
  ],
];

const PATTERNS_11X11 = [
  [
    "...#...#...",
    "...#...#...",
    "...........","###...#..##",
    "......#....",
    "...#...#...",
    "....#......",
    "##..#...###",
    "...........",
    "...#...#...",
    "...#...#...",
  ],
  [
    "....#.#....",
    "...........","..#.....#..",
    "#..#...#..#",
    "....#.#....",
    ".#.......#.",
    "....#.#....",
    "#..#...#..#",
    "..#.....#..",
    "...........",
    "....#.#....",
  ],
];

const PATTERNS_13X13 = [
  [
    "....#...#....",
    "....#...#....",
    ".............",
    "###....#..###",
    "...#.....#...",
    "....#...#....",
    ".............","....#...#....",
    "...#.....#...",
    "###..#....###",
    ".............",
    "....#...#....",
    "....#...#....",
  ],
];

const PATTERNS_15X15 = [
  // Classic NYT-style pattern
  [
    "...##...#..#...",
    "....#...#..#...",
    "........#......",
    "###..#.........",
    "...#....##..###",
    "....#.....#....",
    "..#...###...#..",
    "...............",
    "..#...###...#..",
    "....#.....#....",
    "###..##....#...",
    ".........#..###",
    "......#........",
    "...#..#...#....",
    "...#..#...##...",
  ],
  // More open pattern
  [
    "....#....#.....",
    "....#....#.....",
    "....#....#.....",
    "###...###...###",
    "...............",
    "#....#...#....#",
    "...............",
    "###...###...###",
    "...............",
    "#....#...#....#",
    "...............",
    "###...###...###",
    ".....#....#....",
    ".....#....#....",
    ".....#....#....",
  ],
];

export function getPatterns(size: number): string[][] {
  switch (size) {
    case 5:
      return PATTERNS_5X5;
    case 11:
      return PATTERNS_11X11;
    case 13:
      return PATTERNS_13X13;
    case 15:
      return PATTERNS_15X15;
    default:
      return PATTERNS_15X15;
  }
}
